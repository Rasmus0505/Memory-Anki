def read_text(path: Path) -> str:
    if not path.exists():
        return ""
    return path.read_text(encoding="utf-8-sig")


def write_text(relative: str, content: str) -> None:
    path = ROOT / relative
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(content.strip() + "\n", encoding="utf-8")


def has_h1(text: str) -> bool:
    return re.search(r"(?m)^#\s+\S", text) is not None


def title_from_path(path: Path) -> str:
    return "文档库入口总览" if path.stem == "README" else path.stem


def prepend_h1_if_missing(path: Path, original_relative: str) -> None:
    text = read_text(path)
    if has_h1(text):
        return
    title = title_from_path(path)
    path.write_text(
        f"# {title}\n\n> 原始路径：{original_relative}\n\n{text.strip()}\n",
        encoding="utf-8",
    )


